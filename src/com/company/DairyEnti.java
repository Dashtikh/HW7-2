package com.company;

public class DairyEnti {

    private int id;
    private String name;
    private int price;


    public int getId() {
        return id;
    }

    public DairyEnti setmodel(int id) {
        this.id = id;
        return this;
    }

    public String getName() {
        return name;
    }

    public DairyEnti setName(String name) {
        this.name = name;
        return this;
    }

    public int getPrice() {
        return price;
    }

    public DairyEnti setprice(int price) {
        this.price = price;
        return this;
    }


}
